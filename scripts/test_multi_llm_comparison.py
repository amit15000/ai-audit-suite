async def test_generating_new_responses():
    """Test generating new responses when user only selected 1 platform (FALLBACK CASE).
    
    NOTE: This is a FALLBACK scenario. In normal API flow:
    - User selects multiple platforms → responses already generated → use those
    - This test only runs if user selected only 1 platform (rare case)
    """
    print("\n" + "="*80)
    print("TEST 3: Fallback - Generating New Responses (User Selected Only 1 Platform)")
    print("="*80)
    print("\n⚠️  NOTE: This is a FALLBACK case. In normal flow, user selects multiple platforms.")
    print("   → If user selects only 1 platform, we generate from remaining LLMs")
    print("   → This requires API keys and makes additional API calls")
    print("   → In production, users typically select 2-3 platforms")
    
    scorer = MultiLLMComparisonScorer()
    
    original_prompt = "Explain quantum computing in simple terms."
    target_platform_id = "openai"
    
    # Simulate only target response available (user selected only 1 platform)
    all_responses = {
        "openai": "Quantum computing uses quantum mechanical phenomena like superposition and entanglement to perform computations. Unlike classical computers that use bits (0 or 1), quantum computers use qubits that can exist in multiple states simultaneously."
    }
    
    print(f"\n📋 Scenario: User selected only [{target_platform_id}]")
    print(f"   → Only 1 response available in all_responses")
    print(f"   → Will generate from remaining LLMs (gemini, groq)")
    
    print(f"\nTarget Platform: {target_platform_id}")
    print(f"Target Response: {all_responses[target_platform_id][:100]}...")
    
    # Check for API keys
    has_openai = bool(os.getenv("OPENAI_API_KEY"))
    has_gemini = bool(os.getenv("GEMINI_API_KEY") or os.getenv("GOOGLE_API_KEY"))
    has_groq = bool(os.getenv("GROQ_API_KEY"))
    
    print(f"\n🔑 API Keys Available:")
    print(f"   - OpenAI: {'✅' if has_openai else '❌'}")
    print(f"   - Gemini: {'✅' if has_gemini else '❌'}")
    print(f"   - Groq: {'✅' if has_groq else '❌'}")
    
    if not (has_gemini or has_groq):
        print("\n⚠️  WARNING: No API keys for gemini/groq. This test will fail.")
        print("   Set GEMINI_API_KEY or GROQ_API_KEY to test fallback generation.")
        return
    
    print("\n🔄 Calculating multi-LLM comparison score (will generate new responses)...")
    try:
        detailed = await scorer.get_detailed_comparison(
            target_response=all_responses[target_platform_id],
            original_prompt=original_prompt,
            target_platform_id=target_platform_id,
            judge_platform_id="openai",
            use_llm=True,
            all_responses=all_responses,  # Only has target response
        )
        
        print("\n" + "-"*80)
        print("DETAILED RESULTS:")
        print("-"*80)
        print(f"Score: {detailed['score']}/10")
        print(f"Consensus Alignment: {detailed.get('consensus_alignment', 0):.1f}%")
        print(f"Reference LLMs Used: {', '.join(detailed.get('reference_llms_used', []))}")
        print(f"\nExplanation: {detailed.get('explanation', 'N/A')[:200]}...")
        
        if detailed.get('reference_llms_used'):
            print(f"\n✅ Successfully generated responses from: {', '.join(detailed['reference_llms_used'])}")
        else:
            print(f"\n⚠️  No reference responses generated (check API keys and rate limits)")
        
    except Exception as e:
        print(f"\n❌ Error: {str(e)}")
        print("\n💡 Tip: In production, users select multiple platforms, so this fallback is rarely needed.")
        import traceback
        traceback.print_exc()


