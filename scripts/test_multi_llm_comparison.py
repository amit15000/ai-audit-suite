async def test_with_existing_responses():
    """Test multi-LLM comparison using existing responses (REAL API FLOW).
    
    This simulates the real API flow:
    1. User hits /comparison/submit with platforms: ["openai", "gemini", "groq"]
    2. API generates responses from all selected platforms
    3. When calculating scores, all_responses contains all generated responses
    4. Multi-LLM comparison uses these existing responses (NO new API calls needed)
    """
    print("\n" + "="*80)
    print("TEST 1: Real API Flow - Using Existing Responses")
    print("="*80)
    print("\n📋 Scenario: User selected [openai, gemini, groq] in /comparison/submit")
    print("   → API already generated responses from all platforms")
    print("   → all_responses contains: {openai: '...', gemini: '...', groq: '...'}")
    print("   → Multi-LLM comparison uses these existing responses (efficient!)")
    
    scorer = MultiLLMComparisonScorer()  # No ai_service needed
    
    original_prompt = "What is the capital of France and its population?"
    target_platform_id = "openai"
    
    # Simulate responses that were already generated by the API
    print("\n📝 Simulating responses already generated by API...")
    all_responses = {
        "openai": "The capital of France is Paris. The population of Paris is approximately 2.1 million people.",
        "gemini": "The capital of France is Paris. Paris has a population of around 2.1 million residents.",
        "groq": "Paris is the capital city of France. The population of Paris is about 2.1 million people."
    }
    
    print(f"\n✅ Target Platform: {target_platform_id}")
    print(f"✅ Target Response: {all_responses[target_platform_id]}")
    print(f"\n✅ Existing Responses (from API):")
    for platform, response in all_responses.items():
        if platform != target_platform_id:
            print(f"   - {platform}: {response[:80]}...")
    
    print("\n🔄 Calculating multi-LLM comparison score...")
    print("   → Using existing responses from all_responses (NO new API calls)")
    try:
        score = await scorer.calculate_score(
            target_response=all_responses[target_platform_id],
            original_prompt=original_prompt,
            target_platform_id=target_platform_id,
            judge_platform_id="openai",
            use_llm=True,
            all_responses=all_responses,  # Pass existing responses from API
        )
        
        print(f"\n✅ Score: {score}/10")
        
        # Get detailed results
        print("\n📊 Getting detailed comparison results...")
        detailed = await scorer.get_detailed_comparison(
            target_response=all_responses[target_platform_id],
            original_prompt=original_prompt,
            target_platform_id=target_platform_id,
            judge_platform_id="openai",
            use_llm=True,
            all_responses=all_responses,
        )
        
        print("\n" + "-"*80)
        print("DETAILED RESULTS:")
        print("-"*80)
        print(f"Score: {detailed['score']}/10")
        print(f"Consensus Alignment: {detailed.get('consensus_alignment', 0):.1f}%")
        print(f"Unique Claims Count: {detailed.get('unique_claims_count', 0)}")
        print(f"Contradictory Claims Count: {detailed.get('contradictory_claims_count', 0)}")
        print(f"Consensus Claims Count: {detailed.get('consensus_claims_count', 0)}")
        print(f"Reference LLMs Used: {', '.join(detailed.get('reference_llms_used', []))}")
        print(f"   → These are from existing all_responses (no new generation)")
        print(f"\nExplanation: {detailed.get('explanation', 'N/A')}")
        
        if detailed.get('unique_claims'):
            print("\n🔍 UNIQUE CLAIMS (Only in target):")
            for i, claim in enumerate(detailed['unique_claims'], 1):
                print(f"\n  {i}. Claim: {claim.get('claim', 'N/A')}")
                print(f"     Severity: {claim.get('severity', 'N/A')}")
                print(f"     Explanation: {claim.get('explanation', 'N/A')}")
        
        if detailed.get('contradictory_claims'):
            print("\n⚠️  CONTRADICTORY CLAIMS:")
            for i, claim in enumerate(detailed['contradictory_claims'], 1):
                print(f"\n  {i}. Target Claim: {claim.get('target_claim', 'N/A')}")
                print(f"     Consensus Claim: {claim.get('consensus_claim', 'N/A')}")
                print(f"     Consensus Count: {claim.get('consensus_count', 0)}")
                print(f"     Severity: {claim.get('severity', 'N/A')}")
                print(f"     Explanation: {claim.get('explanation', 'N/A')}")
        
        if detailed.get('consensus_claims'):
            print("\n✅ CONSENSUS CLAIMS (All agree):")
            for i, claim in enumerate(detailed['consensus_claims'][:5], 1):  # Show first 5
                print(f"\n  {i}. Claim: {claim.get('claim', 'N/A')}")
                print(f"     Agreement: {claim.get('agreement_count', 0)}/{claim.get('total_responses', 0)}")
        
        print("\n✅ SUCCESS: Used existing responses from all_responses (efficient!)")
        
    except Exception as e:
        print(f"\n❌ Error: {str(e)}")
        import traceback
        traceback.print_exc()


